    public void setComponent(){
        this.lbID.setText(user.getId().toString());
        this.txtCpf.setText(user.getCpf());
        this.txtEmail.setText(user.getEmail());
        this.txtLogin.setText(user.getLogin());
        this.txtName.setText(user.getName());
        this.txtNeighborhood.setText(user.getNeighborhood());
        this.txtNumberHouser1.setText(String.valueOf(user.getNumberhouse()));
        this.txtPassword.setText(user.getPassword());
        this.txtStreet.setText(user.getStreet());
        this.txtTelephone.setText(user.getTelephone());
        this.txtType.setText(user.getType());
        if(user.getSex() == sex.Female){
            this.cbnSex.setSelectedIndex(1);
        }else{
            this.cbnSex.setSelectedIndex(0);
        }
        if(user.getStatus() == 1){
            this.cmbStatus.setSelectedIndex(0);
        }else{
            this.cmbStatus.setSelectedIndex(1);
        }
    }
    
    public void getComponent(){
        user.setCpf(this.txtCpf.getText());
        user.setEmail(this.txtEmail.getText());
        user.setLogin(this.txtLogin.getText());
        user.setName(this.txtName.getText());
        user.setNeighborhood(this.txtNeighborhood.getText());
        user.setNumberhouse(Integer.valueOf(this.txtNumberHouser1.getText()));
        user.setPassword(String.valueOf(this.txtPassword.getPassword()));
        user.setStreet(this.txtStreet.getText());
        user.setTelephone(this.txtTelephone.getText());
        user.setType(this.txtType.getText());
        user.setSex(Sex.valueOf(this.cbnSex.getSelectedItem().toString()));
        if(this.cmbStatus.getSelectedItem().toString() == "Activate"){
            user.setStatus(1);
        }else{
            user.setStatus(0);
        }
        
    }
    

