    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        List<User> list = repository.Search(null);
        int value = 0;
        

        for(User user : list){
            if(this.user.getLogin() != user.getLogin())
                if(user.getLogin().equals(this.txtLogin.getText()) == true)//Dessa forma se compara strings
                    value = 1;
        }
        
        if(!this.txtCpf.getText().isEmpty() && !this.txtLogin.getText().isEmpty() && !this.txtName.getText().isEmpty() && !this.txtPassword.getPassword().toString().isEmpty()){
            if(value == 0){
                if(JOptionPane.showConfirmDialog(this, "Really want to save?", "Confirm", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION){
                    this.getComponent();
                    if(repository.Save(user) == true){
                        JOptionPane.showMessageDialog(this, "User saved!","Information", JOptionPane.INFORMATION_MESSAGE);
                        this.setComponent();
                    }else{
                        JOptionPane.showMessageDialog(this, "User is not saved!","Erro", JOptionPane.ERROR_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Operation canceled!","Information", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "This login already exists!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "The login, password, CPF and name must be completed!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        this.user = new User();
    }//GEN-LAST:event_btnSaveActionPerformed

