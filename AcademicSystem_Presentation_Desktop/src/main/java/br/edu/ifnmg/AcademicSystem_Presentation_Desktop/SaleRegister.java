    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(!this.txtAmount.getText().isEmpty() && !this.txtProductName.getText().isEmpty()){
            try {
                Product product = repositoryProduct.ProductName(this.txtProductName.getText());
                if(product != null){
                    if((product.getSalereason() * Integer.valueOf(this.txtAmount.getText())) <= product.getStock()){
                        ItemSale item = new ItemSale(product,Integer.valueOf(this.txtAmount.getText()),this.sale);
                        this.listItem.add(item);
                        this.setTable();
                        this.txtAmount.setText("");
                        this.txtProductName.setText("");
                    }else{
                        JOptionPane.showMessageDialog(null, "The quantity exceeds the quantity present in the stock!", "Information", JOptionPane.INFORMATION_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Product is not exist!","Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch(Exception ex){
                JOptionPane.showMessageDialog(this, "Product is not exist: !" + ex.toString(),"Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "The Amount and Product Name of purchase must be completed!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        //this.updateValeu(1);
    }//GEN-LAST:event_btnAddActionPerformed


